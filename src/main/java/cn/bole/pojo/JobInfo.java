package cn.bole.pojo;

public class JobInfo {
	private String jobInfoId;
	private String discription;
	private Integer orderNo;
	private Integer salaryRange;
	private Integer welfare;
	private String city;
	public String getJobInfoId() {
		return jobInfoId;
	}
	public void setJobInfoId(String jobInfoId) {
		this.jobInfoId = jobInfoId;
	}
	public String getDiscription() {
		return discription;
	}
	public void setDiscription(String discription) {
		this.discription = discription;
	}
	public Integer getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(Integer orderNo) {
		this.orderNo = orderNo;
	}
	public Integer getSalaryRange() {
		return salaryRange;
	}
	public void setSalaryRange(Integer salaryRange) {
		this.salaryRange = salaryRange;
	}
	public Integer getWelfare() {
		return welfare;
	}
	public void setWelfare(Integer welfare) {
		this.welfare = welfare;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
	
	
	
	  
	   
}
